<template>
  <div class="counter">
    <button @click="subtract()">
      -
    </button>
    <pre>{{ count }}</pre>
    <button @click="add()">
      +
    </button>
  </div>
  <div class="counter-message">
    <slot />
  </div>
</template>

<script setup lang="ts">
import { ref } from 'vue';

const count = ref(0);
const add = () => (count.value = count.value + 1);
const subtract = () => (count.value = count.value - 1);
</script>

<style>
.counter {
  display: grid;
  grid-template-columns: repeat(3, minmax(0, 1fr));
  margin-top: 2em;
  font-size: 2em;
  place-items: center;
}

.counter-message {
  text-align: center;
}
</style>
